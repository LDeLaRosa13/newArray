var drinks = ["pepsi", "coke", "sprite", "rootbeer"];
//the .pop method will remove the rootbeer element from this array
drinks.pop();
console.log(drinks);


var numbers = ["11", "12", "13", "18"];
//the .shift method will remove the element in the [0] placement
numbers.shift();
console.log(numbers);

var thoughts = ["true", "false", "false", "true"];
//the .push method would return the array back to its original length
thoughts.push();
console.log(thoughts);
